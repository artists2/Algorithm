# 최대공약수 예시
# 1071은 1029로 딱 나눠지지 않기 때문에 1071을 1029로 나눈 나머지를 구함 -> 42
# 1029는 42로 나눠지지 않기 때문에 1029를 42로 나눈 나머지를 구함 -> 21
# 42는 21로 나눠지기 때문에 최대공약수는 21


